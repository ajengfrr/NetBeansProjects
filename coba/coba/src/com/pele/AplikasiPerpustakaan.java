    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        int nim = Integer.parseInt(txtNim.getText());
        String nama = txtNama.getText();
        String kelamin = cbKelamin.getSelectedItem().toString();
        String fakultas = cbFakultas.getSelectedItem().toString();
        long tglPinjam = dcTglPinjam.getDate().getTime();
        long tglKembali = dcTglKembali.getDate().getTime();
        String judul = txtJudul.getText();
        String pengarang = txtPengarang.getText();
        
        java.sql.Date tglpinjam = new java.sql.Date(tglPinjam);
        java.sql.Date tglkembali = new java.sql.Date(tglKembali);
        try {
            Connection conn = getConnection();
            conn.createStatement().executeUpdate("INSERT INTO MAHASISWA VALUES ('"+nim+"','"+nama+"','"+kelamin+"','"+fakultas+"','"+tglpinjam+"','"+judul+"','"+pengarang+"','"+tglkembali+"')");
        } catch (SQLException e) {
            System.out.println("Ada error di saveMahasiswa dengan pesan: " + e.getMessage());
        }
        showMahasiswa();
        resetForm();
    }//GEN-LAST:event_btnSimpanActionPerformed

