    public Connection getConnection(){
        Connection connection = null;
        try {
            String url = "jdbc:mysql://localhost:3306/coba";
            String user = "root";
            String pass = "";
            DriverManager.registerDriver(new com.mysql.jdbc.Driver());
            connection = DriverManager.getConnection(url, user, pass);

        } catch (SQLException e) {
            System.out.println("Ada error dengan pesan: " + e.getMessage());
        }
        return connection;
    }
    
    public void showMahasiswa(){
        int row= tabelMahasiswa.getRowCount();
        for (int i = 0; i < row; i++) {
            tableModel.removeRow(0);
        }
        try {
            String sql = "SELECT * FROM MAHASISWA";
            Connection conn = getConnection();
            ResultSet res = conn.createStatement().executeQuery(sql);
            while(res.next()){
                tableModel.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getDate(5),res.getString(6),res.getString(7),res.getDate(8)});
            }
            tabelMahasiswa.setModel(tableModel);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Terjadi gagal pada show mahasiswa");
        }
    }

